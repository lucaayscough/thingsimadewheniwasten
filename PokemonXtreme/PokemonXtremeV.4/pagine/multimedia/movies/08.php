Il film inizia con il racconto di una battaglia tra due eserciti, composti da uomini e Pokémon, svolta nei pressi del regno di Sir Aaron e della regina Lady Rin. Avvertito da Lucario dei rischi del conflitto, Sir Aaron si reca nel luogo della battaglia, rivela al Pokémon di aver abbandonato il ruolo di Maestro e lo imprigiona nel suo bastone che verrà consegnato alla regina da Pidgeot. Nel frattempo, proprio quando le due armate iniziano lo scontro, un potere magico provvederà a fermare i guerrieri. La leggenda narra che fu Sir Aaron a ristabilire la pace.
Molti secoli dopo Ash e i suoi amici raggiungono il Palazzo di Cameron, dove è in corso una festa in onore di Sir Aaron che comprende un torneo di Pokémon in maschera. In città è presente anche il Pokémon leggendario Mew che, con la capacità di assumere varie forme si presenta a Pikachu sotto forma di Pichu, di Treecko ed infine di Aipom.
Ash, con l'aiuto di Pikachu, riesce a sconfiggere nella finale del torneo un Weavile e viene nominato "Guardiano dell'Aura", una carica annuale che permette all'allenatore di tenere tra le mani il bastone di Sir Aaron. Ash e Vera liberano i loro Pokémon per farli assistere alla festa, in compagnia di Mew (sotto forma di Aipom). Anche Meowth del Team Rocket li pedinerà, ma la sua attenzione verrà attirata da Kidd Summers, l'allenatrice che Ash ha sconfitto durante il torneo.
I Pokémon di Ash e Vera vengono accompagnati dal finto Aipom in una soffitta del Palazzo piena di giochi. Divertito, Mew si trasforma prima in Pikachu ed infine rivela la sua vera forma una volta che Meowth piomba nella stanza dalla finestra, dopo aver perso l'equilibrio mentre seguiva Kidd sui tetti del Palazzo. Quest'ultima entra in azione vedendo Mew ed invia i suoi due Weavile ad applicare un trasmettitore al Pokémon leggendario. Tuttavia Pikachu tenta di difendere Mew, che provvede a trasformarsi in Meowth per confondere i suoi inseguitori. Anche Max, seguendo Munchlax, ha nel frattempo raggiunto la stanza e vede il Pokémon teletrasportarsi insieme a Pikachu e Meowth. Mew infine si trasforma in Pidgeot e fugge.
Al termine delle danze, Ash assume la posa caratteristica di Sir Aaron, basata su un dipinto presente nel Palazzo. L'Aura di Ash tuttavia libera Lucario, convinto di essere rimasto prigioniero nel bastone solamente pochi giorni. Dopo un colloquio tra la regina, Lady Ilene, ed il Pokémon, Ash viene a sapere che Max ha visto Mew e che quest'ultimo ha condotto Pikachu nell'Albero dell'Inizio. Lucario e l'avventuriera Kidd Summers, il cui scopo è visitare l'Albero e scoprire il mistero di Mew, aiuteranno Ash a ritrovare il suo Pokémon.
Lucario tuttavia prova ancora rancore nei confronti del suo Maestro, a causa dell'abbandono, ed in generale verso tutti gli esseri umani, compreso Ash che subirà un tentativo di aggressione da parte del Pokémon. Nonostante ciò l'indomani Lucario guida con la sua Aura il fuoristrada di Kidd, il cui portabagagli ospita Jessie e James. Quando i ragazzi si fermano per il pranzo, trovano un esemplare di Bonsly che li accompagnerà nel viaggio. Nel frattempo Mew, Meowth e Pikachu continuano a giocare tranquilli.
Durante una pausa presso una sorgente termale Vera nota un fiore, successivamente raccolto da Ash, detto "Fiore del Tempo". Questa pianta ha la particolarità di mostrare avvenimenti del passato a chi possiede l'Aura. Nel frattempo Lucario continua a ricordare le sue esperienze con Sir Aaron e questo lo porterà a litigare nuovamente con Ash. Dopo una breve lotta, Vera sgriderà Ash e Max porterà del cioccolato al Pokémon.
La ricerca proseguirà fino a quando Lucario tornerà nel luogo in cui è stato imprigionato e farà rivivere l'episodio ad Ash ed i suoi amici. L'allenatore si scuserà con il Pokémon, che subito fiuta un pericolo: l'arrivo di Regirock che ha deciso di attaccare il fuoristrada. Lucario lo colpisce con una delle sue mosse e l'intera compagnia si rifugia all'interno dell'Albero.
Nell'Albero dell'Inizio vivono numerose specie di Pokémon e Kidd vorrebbe iniziare le sue ricerche sull'habitat. Tuttavia i guardiani dell'Albero si oppongono alla presenza di esseri umani: Registeel colpisce le attrezzature e Regice impedisce ad Ash di raggiungere Pikachu. Inoltre il sistema immunitario dell'Albero inizia ad utilizzare i suoi globuli bianchi per risucchiare coloro che vengono considerati "germi".
Ash tenta di fermare i Regi utilizzando Corphish e Grovyle e riesce a raggiungere Pikachu, nonostante il forte vento, grazie anche all'aiuto di Kidd. Lucario tenta di portarli in salvo, ma viene fermato da Registeel, mentre Kidd ed Ash vengono attaccati dai leucociti. Grazie all'intervento di Mew, tutti gli umani dell'albero (compresi i membri del Team Rocket) verranno salvati.
Tuttavia l'intero ecosistema, per via dello sforzo al fine di scacciare gli intrusi, inizia a collassare. Anche Mew, che vive in simbiosi con l'Albero, si ammala ma riesce a condurre i suoi amici presso il cuore dello stesso. In quel luogo Lucario trova anche Sir Aaron cristallizzato. Un Fiore del Tempo svelerà che Aaron ha donato la sua Aura al Pokémon leggendario per salvare la popolazione del luogo. Lucario decide quindi di effettuare lo stesso sforzo per salvare l'Albero, ma non essendo sufficiente l'energia, anche Ash interviene per salvare i Pokémon che vivono in quell'habitat.
Prima di completare il processo, Lucario allontana Ash, in modo da risparmiare la vita del ragazzo e sacrificarsi da solo. Prima di morire, un altro Fiore del Tempo mostra a Lucario il suo Maestro in fin di vita che rivela di ritenere il Pokémon più che uno studente, il suo migliore amico. Lucario, dopo aver ricambiato i sentimenti nei confronti di Aaron, si dissolve con lui ed entra a far parte del circolo vitale dell'Albero.
Ash riferisce la sorte di Lucario a Max, Vera e Brock e spiega che l'Aura del Pokémon è ancora dentro di lui. Nei titoli di coda i ragazzi vengono riaccompagnati al Palazzo da Kidd e notano che nel quadro che raffigurava Sir Aaron adesso è presente, al suo fianco, anche Lucario e che nell'Albero dell'Inizio non solo è ritornata la tranquillità, ma anche che Mew ha trovato in Bonsly un nuovo compagno di giochi.

